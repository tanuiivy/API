// Variable explicitly set to null
var nullValue = null;

// Variable declared but not initialized (undefined)
var undefinedValue;

// Log both variables to the console
console.log('Null value:', nullValue);        // Output: Null value: null
console.log('Undefined value:', undefinedValue); // Output: Undefined value: undefined

// Alert to confirm that the script is running
alert('JavaScript is running!');
